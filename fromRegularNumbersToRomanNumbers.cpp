#include <iostream>
#include <string>

using namespace std;

int main() {
    int number; // Переменная для хранения введенного числа
    string roman = ""; // Строка для римской записи

    // Запрашиваем у пользователя ввод числа
    cout << "Enter an integer between 1 and 3999: ";
    cin >> number;

    // Проверяем корректность введенного числа
    if (number < 1 || number > 3999) {
        cout << "Please enter a valid number between 1 and 3999." << endl;
        return 1; // Завершаем программу с ошибкой
    }

    // Обработка тысяч
    while (number >= 1000) {
        roman += "M"; // Добавляем 'M' для каждой тысячи
        number -= 1000; // Уменьшаем число на 1000
    }

    // Обработка девятисот
    if (number >= 900) {
        roman += "CM"; // Добавляем 'CM'
        number -= 900; // Уменьшаем число на 900
    }

    // Обработка пятисот
    if (number >= 500) {
        roman += "D"; // Добавляем 'D'
        number -= 500; // Уменьшаем число на 500
    }

    // Обработка четырёхсот
    if (number >= 400) {
        roman += "CD"; // Добавляем 'CD'
        number -= 400; // Уменьшаем число на 400
    }

    // Обработка сотен
    while (number >= 100) {
        roman += "C"; // Добавляем 'C'
        number -= 100; // Уменьшаем число на 100
    }

    // Обработка девяток
    if (number >= 90) {
        roman += "XC"; // Добавляем 'XC'
        number -= 90; // Уменьшаем число на 90
    }

    // Обработка пятидесяти
    if (number >= 50) {
        roman += "L"; // Добавляем 'L'
        number -= 50; // Уменьшаем число на 50
    }

    // Обработка сорока
    if (number >= 40) {
        roman += "XL"; // Добавляем 'XL'
        number -= 40; // Уменьшаем число на 40
    }

    // Обработка десятков
    while (number >= 10) {
        roman += "X"; // Добавляем 'X'
        number -= 10; // Уменьшаем число на 10
    }

    // Обработка девяток в единицах
    if (number == 9) {
        roman += "IX"; // Добавляем 'IX'
        number -= 9;   // Уменьшаем число на 9
    }

    // Обработка пятёрок в единицах
    if (number >= 5) {
        roman += "V"; // Добавляем 'V'
        number -= 5; // Уменьшаем число на 5
    }

    // Обработка четвёрок в единицах
    if (number == 4) {
        roman += "IV"; // Добавляем 'IV'
        number -= 4;   // Уменьшаем число на 4
    }

    // Обработка единиц
    while (number >= 1) {
        roman += "I"; // Добавляем 'I'
        number -= 1;   // Уменьшаем число на 1
    }

    // Выводим результат на экран
    cout << "The Roman numeral representation is: " << roman << endl;

    return 0; // Завершаем программу успешно
}
